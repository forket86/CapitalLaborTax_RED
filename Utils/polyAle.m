function value = polyAle(order,x,w)

[row,col]=size(x);
value=zeros(row,1);

if col==3
    for i=0:order
        value(:)=value(:)+w(i+1,1)*x(:,1).^i+w(i+1,2)*x(:,2).^i+w(i+1,3)*x(:,3).^i...
            +w(i+1,4)*(x(:,1).*x(:,2)).^i+w(i+1,5)*(x(:,2).*x(:,3)).^i;
    end
end

if col==4
    for i=0:order
        value(:)=value(:)+w(i+1,1)*x(:,1).^i+w(i+1,2)*(x(:,2)).^i+w(i+1,3)*(x(:,3)).^i+w(i+1,4)*(x(:,4)).^i...
                         +w(i+1,5)*(x(:,1).*x(:,2)).^i+w(i+1,6)*(x(:,2).*x(:,3)).^i+w(i+1,7)*(x(:,3).*x(:,4)).^i...
                         +w(i+1,8)*(x(:,1).*x(:,3)).^i+w(i+1,9)*(x(:,1).*x(:,4)).^i+w(i+1,10)*(x(:,2).*x(:,4)).^i...
                         +w(i+1,11)*(x(:,1).*x(:,2).*x(:,3)).^i+w(i+1,12)*(x(:,1).*x(:,2).*x(:,4)).^i+w(i+1,13)*(x(:,2).*x(:,3).*x(:,4)).^i...
                         +w(i+1,14)*(x(:,1).*x(:,2).*x(:,3).*x(:,4)).^i;
    end
end

end

